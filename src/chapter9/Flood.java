    private static int[][] MAP;
    private static int N;
    private static int M;
    public static int floodFill(int[][] map, int n, int m) {
        MAP = map;
        N = n;
        M = m;
        int[][] res = new int[n][m];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m;j++){
                res[i][j] =-1;
            }
        }
        flood(res,0,0,0);
        return res[n-1][m-1];
    }

    private static void flood(int[][] res, int x, int y, int time) {
        if (MAP[x][y] == 1)
            res[x][y] = Integer.MAX_VALUE;
        if (res[x][y] != -1) return;
        res[x][y] = time;
        if(x!=N-1) flood(res,x+1,y,time+1);
        if(y!=0) flood(res,x,y-1,time+1);
        if(y!= M-1) flood(res,x,y+1,time+1);
    }

